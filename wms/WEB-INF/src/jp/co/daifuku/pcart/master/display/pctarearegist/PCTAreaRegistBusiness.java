    /**
     *
     * @throws Exception
     */
    private void btn_Set_Click_Process(String eventSource)
            throws Exception
    {
        // input validation.
        txt_Area.validate(this, true);
        txt_AreaName.validate(this, false);

        // DFKLOOK start
        if (StringUtil.isBlank(eventSource))
        {         
            // 登録しますか?
            this.setConfirm("MSG-W0012", false, true);
            viewState.setString(_KEY_CONFIRMSOURCE, "btn_Set_Click");
            return;         
        }
        // DFKLOOK end
        
        // get locale.
        DfkUserInfo ui = (DfkUserInfo)getUserInfo();
        Locale locale = httpRequest.getLocale();

        Connection conn = null;
        PCTAreaRegistSCH sch = null;
        try
        {
            // open connection.
            conn = ConnectionManager.getRequestConnection(this);
            sch = new PCTAreaRegistSCH(conn, this.getClass(), locale, ui);

            // set input parameters.
            PCTAreaRegistSCHParams inparam = new PCTAreaRegistSCHParams();
            inparam.setProcessFlag(ProcessFlag.REGIST);
            inparam.set(PCTAreaRegistSCHParams.AREA_NO, txt_Area.getValue());
            inparam.set(PCTAreaRegistSCHParams.AREA_NAME, txt_AreaName.getValue());

            // SCH call.
            if (!sch.startSCH(inparam))
            {
                // rollback.
                conn.rollback();
                message.setMsgResourceKey(sch.getMessage());
                return;
            }

            // commit.
            conn.commit();
            message.setMsgResourceKey(sch.getMessage());
        }
        catch (Exception ex)
        {
            ex.printStackTrace();
            message.setMsgResourceKey(ExceptionHandler.getDisplayMessage(ex, this));
            if (sch != null && !StringUtil.isBlank(sch.getMessage()))
            {
                message.setMsgResourceKey(sch.getMessage());
            }
        }
        finally
        {
            DBUtil.rollback(conn);
            DBUtil.close(conn);
        }
    }

