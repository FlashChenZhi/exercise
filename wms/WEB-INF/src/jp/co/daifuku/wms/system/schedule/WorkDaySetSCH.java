    /**
     * 画面から入力された内容をパラメータとして受け取り、
     * リストセルエリア出力用のデータをデータベースから取得して返します。<BR>
     *
     * @param p 表示データ取得条件を持つ<CODE>ScheduleParams</CODE><BR>
     * @return 検索結果を持つ<CODE>Params</CODE>配列。<BR>
     *          該当レコードが一件もみつからない場合は要素数0のリストを返します。<BR>
     *          検索結果が最大表示件数を超えた場合は最大表示件数まで表示します<BR>
     *          入力条件にエラーが発生した場合はnullを返します。<BR>
     * @throws CommonException チェック処理内で予期しない例外が発生した場合に通知します。
     */
    public List<Params> query(ScheduleParams p)
            throws CommonException
    {
        // システム定義情報のコンストラクタ
        WarenaviSystemController sysController = new WarenaviSystemController(getConnection(), getClass());

        List<Params> result = new ArrayList<Params>();
        Params param = new Params();

        // システム作業日を検索
        param.set(PRESENT_WORK_DATE, WmsFormatter.toDate(sysController.getWorkDay()));
        result.add(param);

        return result;
    }

    /**
     * 画面から入力された内容をパラメータとして受け取り、スケジュールを開始します。<BR>
     *
     * @param ps 設定内容を持つ<CODE>ScheduleParams</CODE>の配列。<BR>
     * @throws CommonException 全ての例外を報告します
     * @return スケジュールが正常終了した場合はtrue、失敗した場合はfalseを返します。
     */
    public boolean startSCH(ScheduleParams... ps)
            throws CommonException
    {
        // 処理前のチェックを行う。
        // 日次更新チェック
        if (!canStart())
        {
            return false;
        }
        
        // データ取込中チェック
        if (isLoadData())
        {
            return false;
        }
        
        // データ報告中チェック
        if (isReportData())
        {
            return false;
        }
        
        WarenaviSystemController sysController = new WarenaviSystemController(getConnection(), getClass());
        if (!sysController.updateWorkDay(WmsFormatter.toParamDate(ps[0].getDate(CHANGE_WORK_DATE)), getWmsUserInfo()))
        {
            // 6023052=すでに作業を行ったことがある作業日より前の日付への変更は行えません。
            setMessage("6023052");
            return false;
        }

        // 6001006=設定しました。
        setMessage("6001006");
        return true;
    }

