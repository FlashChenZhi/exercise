    /**
     *
     * @param confirm
     * @throws Exception
     */
    private void btn_Print_Click_Process(boolean confirm)
            throws Exception
    {
        // input validation.
        pul_Area.validate(this, true);
        pul_RMNo.validate(this, true);

        // get locale.
        DfkUserInfo ui = (DfkUserInfo)getUserInfo();
        Locale locale = httpRequest.getLocale();

        Connection conn = null;
        AsEmptyShelfListDASCH dasch = null;
        PrintExporter exporter = null;
        try
        {
            // open connection.
            conn = ConnectionManager.getRequestConnection(this);
            dasch = new AsEmptyShelfListDASCH(conn, this.getClass(), locale, ui);
            dasch.setForwardOnly(true);

            // set input parameters.
            AsEmptyShelfListDASCHParams inparam = new AsEmptyShelfListDASCHParams();
            inparam.set(AsEmptyShelfListDASCHParams.AREA, _pdm_pul_Area.getSelectedValue());
            inparam.set(AsEmptyShelfListDASCHParams.RM_NO, _pdm_pul_RMNo.getSelectedValue());

            // check count.
            int count = dasch.count(inparam);
            if (confirm && count > 0)
            {
                // show confirm message.
                this.setConfirm("MSG-W0018\t" + Formatter.getNumFormat(count), false, true);
                viewState.setString(_KEY_CONFIRMSOURCE, "btn_Print_Click");
                return;
            }
            else if (count == 0)
            {
                message.setMsgResourceKey("6003011");
                return;
            }

            // DASCH call.
            dasch.search(inparam);

            // open exporter.
            ExporterFactory factory = new WmsExporterFactory(locale, ui);
            exporter = factory.newPrinterExporter("AsrsEmptyShelfList", false);
            exporter.open();

            // export.
            while (dasch.next())
            {
                Params outparam = dasch.get();
                AsrsEmptyShelfListParams expparam = new AsrsEmptyShelfListParams();
                expparam.set(AsrsEmptyShelfListParams.DFK_DS_NO, outparam.get(AsEmptyShelfListDASCHParams.DFK_DS_NO));
                expparam.set(AsrsEmptyShelfListParams.DFK_USER_ID, outparam.get(AsEmptyShelfListDASCHParams.DFK_USER_ID));
                expparam.set(AsrsEmptyShelfListParams.DFK_USER_NAME, outparam.get(AsEmptyShelfListDASCHParams.DFK_USER_NAME));
                expparam.set(AsrsEmptyShelfListParams.AREA_NO, outparam.get(AsEmptyShelfListDASCHParams.AREA_NO));
                expparam.set(AsrsEmptyShelfListParams.AREA_NAME, outparam.get(AsEmptyShelfListDASCHParams.AREA_NAME));
                expparam.set(AsrsEmptyShelfListParams.SYS_DAY, outparam.get(AsEmptyShelfListDASCHParams.SYS_DAY));
                expparam.set(AsrsEmptyShelfListParams.SYS_TIME, outparam.get(AsEmptyShelfListDASCHParams.SYS_TIME));
                expparam.set(AsrsEmptyShelfListParams.RM_NO, outparam.get(AsEmptyShelfListDASCHParams.RM_NO));
                expparam.set(AsrsEmptyShelfListParams.LOCATION_NO, outparam.get(AsEmptyShelfListDASCHParams.LOCATION_NO));
                if (!exporter.write(expparam))
                {
                    break;
                }
            }

            // execute print.
            try
            {
                exporter.print();
                message.setMsgResourceKey("6001010");
            }
            catch (Exception ex)
            {
                ex.printStackTrace();
                message.setMsgResourceKey("6007034");
                return;
            }

            // write part11 log.
            P11LogWriter part11Writer = new P11LogWriter(conn);
            Part11List part11List = new Part11List();
            part11List.add(_pdm_pul_Area.getSelectedStringValue(), "");
            part11List.add(_pdm_pul_RMNo.getSelectedStringValue(), "");
            part11Writer.createOperationLog(ui, ConvertUtil.objectToInt(EmConstants.OPELOG_CLASS_PRINT), part11List);
            // commit.
            conn.commit();

        }
        catch (Exception ex)
        {
            ex.printStackTrace();
            message.setMsgResourceKey(ExceptionHandler.getDisplayMessage(ex, this));
        }
        finally
        {
            if (dasch != null)
            {
                dasch.close();
            }
            if (exporter != null)
            {
                exporter.close();
            }
            DBUtil.rollback(conn);
            DBUtil.close(conn);
        }
    }

